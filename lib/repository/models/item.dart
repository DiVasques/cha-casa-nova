class Item {
  final String id;
  final String name;
  final double totalPrice;
  final double piecePrice;
  final int totalPieces;
  final int availablePieces;

  Item({
    required this.id,
    required this.name,
    required this.totalPrice,
    required this.piecePrice,
    required this.totalPieces,
    required this.availablePieces,
  });
}
